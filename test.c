#include <stdio.h>

int main() {
    int n, count = 0;

    // Введення натурального числа n
    printf("Введіть натуральне число n: ");
    scanf("%d", &n);

    // Перевірка, що n знаходиться в діапазоні (1 < n < 150)
    if (n <= 1 || n >= 150) {
        printf("Число повинно бути в діапазоні 1 < n < 150\n");
        return 1;
    }

    // Пошук рівних дільників
    for (int m = 1; m < n; m++) {
        if (n / m == n % m) {
            count++;
        }
    }

    // Виведення кількості рівних дільників
    printf("%d\n", count);

    return 0;
}